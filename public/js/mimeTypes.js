var mimeTypes = (function(mt)
{
    mt.store = {};

    mt.set = function(suffixes, mimeType) {
        var suffixesArr = suffixes.split(",");
        for(var i = 0; i < suffixesArr.length; i++) {
            this.store[suffixesArr[i]] = mimeType;
        }
    }

    mt.get = function(suffix) {
        return this.store[suffix];
    }

    return mt;
}(mimeTypes || {}));

